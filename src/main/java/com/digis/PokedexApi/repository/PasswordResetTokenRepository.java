package com.digis.PokedexApi.repository;

import com.digis.PokedexApi.entity.PasswordResetToken;
import java.util.Optional;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;

/**
 *
 * @author ALIEN59
 */
public interface PasswordResetTokenRepository extends JpaRepository<PasswordResetToken, Long> {

    Optional<PasswordResetToken> findByToken(String token);

    @Modifying
    @Query("DELETE FROM PasswordResetToken t WHERE t.usuario.idUsuarioPokemon = :IdUsuarioPokemon")
    void deleteByUsuariioId(Integer IdUsuarioPokemon);

}
